public class DefineBasicInfo {

    public static void main(String[] args) {

        String myName = "Takács Róbert";
        int myAge = 29;
        double myHeight = 177.5;
        boolean marriage = false;

        if (marriage == true) {

            System.out.println("My name is " + myName + ". I'm " + myAge + " years old, " + myHeight + " cm tall, and I'm married! Ugh.");

        } else {

            System.out.println("My name is " + myName + ". I'm " + myAge + " years old, " + myHeight + " cm tall, and I'm happily not married!");
        }

    }
}
